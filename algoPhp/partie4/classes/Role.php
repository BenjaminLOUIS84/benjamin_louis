<?php

class Role {
    ////////////////////////////////////////////Attributes///////////////////////////////////////////
    private string $hero;
     
    ////////////////////////////////////////////Constructor///////////////////////////////////////////

    public function __construct(string $hero){
       
        $this->hero = $hero;
     }
 
    ////////////////////////////////////////////Getter & Setter///////////////////////////////////////////

    public function getHero():string
    {
        return $this-hero;
    }

    public function setHero($hero)
    {
        $this->hero = $hero;
        return $this;
    }
    ////////////////////////////////////////////Method///////////////////////////////////////////

    public function getInfos(){ 
        return $this->getHero();
    }

}


?>



